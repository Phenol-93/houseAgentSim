"""AI 空间诊断报告的中文 prompt 构造工具。"""

from __future__ import annotations

import json
from typing import Any


def build_diagnosis_prompt(
    metrics: dict,
    top_conflicts: list[dict],
    layout_summary: dict,
    family_summary: dict,
) -> str:
    """根据模拟指标、冲突摘要和户型信息构造中文诊断 prompt。"""
    payload = {
        "metrics": metrics,
        "top_conflicts": top_conflicts,
        "layout_summary": layout_summary,
        "family_summary": family_summary,
    }
    data_json = json.dumps(payload, ensure_ascii=False, indent=2, default=str)

    return f"""你是一名面向建筑设计研究的住宅空间行为模拟诊断助手。

你的任务：
根据输入的模拟指标、冲突日志摘要、户型概况和家庭成员概况，生成一份中文空间行为诊断报告。报告应服务于住宅户型优化与课程设计表达，同时呈现“现有优势 / 可保留价值”和“需要改造的问题”。请避免只做负面诊断：优点、保留策略和正向空间价值的篇幅应明显增加，约占正文分析内容的 1/3 到 1/2。

重要约束：
1. 不得直接编造输入中不存在的指标、冲突事件或家庭成员信息。
2. 不得直接生成平面图、CAD 图、户型图或具体绘图指令。
3. 必须基于输入数据进行解释；如果做推断，必须明确说明“基于现有数据推断”。
4. 如果数据不足，必须说明需要补充哪些数据，例如更完整的活动点、冲突日志、路径日志、房间功能、家具位置或改造约束。
5. 输出语言必须为中文，表达风格偏建筑设计研究，避免泛泛而谈。
6. 改造建议应描述空间策略和设计目标，不要假设已经存在未提供的结构条件。
7. 需要优先识别户型中已经表现较好的部分，例如动线较短、冲突较少、功能邻接合理、照护路径清晰、公共空间可达性较好、私密空间相对完整等；这些判断同样必须来自输入数据，或明确标注为“基于现有数据推断”。
8. 如果某类冲突记录为空，可以将其作为“当前模拟中未观察到明显问题”的正向证据，但必须提醒这不等于真实居住中一定没有问题。

输入数据如下：

```json
{data_json}
```

请严格按以下结构输出：

## 1. 原户型现有优势与可保留价值
用 2-4 段或 3-6 条要点说明当前户型中值得保留和强化的空间优点。必须尽量引用 metrics、top_conflicts、layout_summary 或 family_summary 作为依据；若证据不足，请明确说明“基于现有数据只能初步判断”。这一部分应包括：
- 功能布局或活动点设置中的合理之处；
- 对家庭成员日常生活、照护或休息有利的空间条件；
- 模拟结果中未出现或较少出现的冲突所体现的潜在优势；
- 后续改造中应避免破坏的空间关系。

## 2. 原户型主要问题概述
用 1-3 段概述主要空间问题，必须引用输入中的关键指标或冲突类型作为依据。语气上应先承认可保留优势，再指出需要优化的矛盾。

## 3. 问题分项诊断
请按问题逐项展开。每个问题必须包含以下字段：

### 问题名称
给出简洁明确的问题名称。

### 数据证据
列出来自 metrics 或 top_conflicts 的证据。不得引用输入中不存在的数据。

### 空间原因
解释可能的空间组织、功能邻接、动线交叉、家具布置、私密性或夜间安全原因。若证据不足，请说明不确定性。

### 对居住行为的影响
说明对等待、绕行、拥堵、干扰、隐私暴露、老人安全等行为结果的影响。

### 改造目标
提出可复测的目标，例如降低等待、缩短夜间路径、减少工作区干扰、降低隐私暴露等。

### 改造建议
提出建筑设计层面的策略建议，但不要直接生成平面图。

## 4. 优势保留与改造策略衔接
专门说明哪些既有优势需要在改造中保留，哪些问题可以通过微调家具、活动点、门洞可达性、照护辅助设施或功能复合来优化。要求至少列出 3 条“保留或强化策略”，并说明它们如何避免改造过度。

## 5. 优先级排序
按影响程度、出现频率、弱势成员风险和改造紧迫性排序。说明排序理由。

## 6. 改造后复测指标
列出建议在改造后复测的指标，并说明为什么需要复测。只能使用输入中已有指标，或明确标注为“建议新增指标”。

## 7. 适合放入课程设计图纸的摘要文字
提供一段可直接用于课程设计图纸说明的中文摘要，语言应凝练、专业、偏建筑设计研究表达。摘要中必须同时包含“现有优势”和“改造问题”，避免只强调缺陷。
"""


__all__ = ["build_diagnosis_prompt"]
